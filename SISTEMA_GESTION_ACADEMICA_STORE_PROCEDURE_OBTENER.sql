CREATE PROCEDURE GET_USUARIO @ID_USUARIO INT
AS BEGIN

	SELECT [CEDULA]
		  ,[NOMBRE]
		  ,[APELLIDOS]
		  ,[EDAD]
		  ,[CORREO]
		  ,[CODIGODOCENTE]
		  ,[FECHA]
		  ,[ID_ROLLES]
		  ,[ACTIVO]
	  FROM [dbo].[USUARIO]
	  WHERE CEDULA = @ID_USUARIO

END
GO

